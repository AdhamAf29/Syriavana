export const bookings = [];
export const reviews = [];

export function createUser({ name, email, password, phone, address, role }) {
  const exists = users.find(u => u.email.toLowerCase() === email.toLowerCase());
  if (exists) throw new Error("email_in_use");
  const id = `u${users.length + 1}`;
  const passwordHash = bcrypt.hashSync(password, 10);
  const user = { id, name, email, passwordHash, phone: phone || "", address: address || "", role: role || "user", favorites: [], createdAt: new Date().toISOString() };
  users.push(user);
  return user;
}

export function verifyUser(email, password) {
  const user = users.find(u => u.email.toLowerCase() === email.toLowerCase());
  if (!user) return null;
  const ok = bcrypt.compareSync(password, user.passwordHash);
  return ok ? user : null;
}

export function addFavorite(userId, siteId) {
  const u = users.find(x => x.id === userId);
  if (!u) throw new Error("user_not_found");
  if (!u.favorites.includes(siteId)) u.favorites.push(siteId);
  return u;
}

export function removeFavorite(userId, siteId) {
  const u = users.find(x => x.id === userId);
  if (!u) throw new Error("user_not_found");
  u.favorites = u.favorites.filter(s => s !== siteId);
  return u;
}

export function createBooking({ userId, tripId, numberOfPeople, companions, paymentMethod, notes, busType }) {
  const trip = trips.find(t => t.id === tripId && t.active);
  if (!trip) throw new Error("trip_not_found");
  if (trip.seatsAvailable < numberOfPeople) throw new Error("insufficient_seats");
  trip.seatsAvailable -= numberOfPeople;
  const id = `b${bookings.length + 1}`;
  const booking = { id, userId, tripId, numberOfPeople, companions: companions || [], paymentMethod: paymentMethod || "cash", paymentStatus: "pending", bookingStatus: "confirmed", bookingDate: new Date().toISOString(), notes: notes || "", busType: busType || "standard" };
  bookings.push(booking);
  return booking;
}

export function cancelBooking({ bookingId, userId }) {
  const b = bookings.find(x => x.id === bookingId);
  if (!b) throw new Error("booking_not_found");
  if (b.userId !== userId) throw new Error("forbidden");
  if (b.bookingStatus === "cancelled") return b;
  b.bookingStatus = "cancelled";
  const trip = trips.find(t => t.id === b.tripId);
  if (trip) trip.seatsAvailable += b.numberOfPeople;
  return b;
}

export function canReview(userId, tripId) {
  const b = bookings.find(x => x.userId === userId && x.tripId === tripId && x.bookingStatus !== "cancelled");
  return !!b;
}

export function addReview({ userId, tripId, rating, comment }) {
  const id = `r${reviews.length + 1}`;
  const review = { id, userId, tripId, rating, comment: comment || "", reviewDate: new Date().toISOString() };
  reviews.push(review);
  return review;
}
